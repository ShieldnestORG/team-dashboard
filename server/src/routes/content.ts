// ---------------------------------------------------------------------------
// Auth helper — content endpoints require CONTENT_API_KEY
// ---------------------------------------------------------------------------

const CONTENT_API_KEY = process.env.CONTENT_API_KEY || "";

function requireContentKey(
  req: import("express").Request,
  res: import("express").Response,
  next: import("express").NextFunction,
) {
  // Board (session) users always pass — they're already authenticated via the actor middleware
  if (req.actor?.type === "board") {
    next();
    return;
  }
  if (!CONTENT_API_KEY) {
    res.status(503).json({ error: "Content API key not configured" });
    return;
  }
  const provided =
    req.headers["x-content-key"] as string | undefined ??
    req.headers["authorization"]?.replace("Bearer ", "");
  if (provided !== CONTENT_API_KEY) {
    res.status(401).json({ error: "Invalid or missing content API key" });
    return;
  }
  next();
}

// ---------------------------------------------------------------------------
// Public rate limiter — 5 requests per IP per hour, no deps
// ---------------------------------------------------------------------------

const publicRateLimits = new Map<string, { count: number; resetAt: number }>();
const PUBLIC_RATE_LIMIT = 5;
const PUBLIC_RATE_WINDOW_MS = 60 * 60 * 1000; // 1 hour

function checkPublicRateLimit(ip: string): boolean {
  const now = Date.now();
  const entry = publicRateLimits.get(ip);
  if (!entry || now > entry.resetAt) {
    publicRateLimits.set(ip, { count: 1, resetAt: now + PUBLIC_RATE_WINDOW_MS });
    return true;
  }
  if (entry.count >= PUBLIC_RATE_LIMIT) return false;
  entry.count++;
  return true;
}

// Cleanup stale entries every 10 minutes
setInterval(() => {
  const now = Date.now();
  for (const [ip, entry] of publicRateLimits) {
    if (now > entry.resetAt) publicRateLimits.delete(ip);
  }
}, 10 * 60 * 1000);

